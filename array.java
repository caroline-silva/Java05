public class array {
	
	public static void main(String[] args) {
		String[] carros = {"Volvo", "BMW", "Ford", "Mazda"}; //iniciando array string
		int [] numeros = {3, 5 , 20, 76, 82, 53}; //inicinado array numeros
		System.out.println(carros[3]); //aparece apenas dado de um indice do array
		System.out.println("tamanho carros = " + carros.length); //tamanho do array
		
		System.out.println(numeros[3]); //aparece apenas dado de um indice do array
		System.out.println("tamanho numeros = " + numeros.length); //tamanho do array
		
	}

}
